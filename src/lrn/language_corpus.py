# Grade thresholds for vocabulary
GRADE_THRESHOLDS = {
    "grade1": 250,
    "grade2": 500,
    "grade3": 1000,
    "grade4": 2000,
    "grade5": 3500,
    "grade6": 5000,  # Fluent polyglot
}


def get_grade(word_count: int) -> str:
    """Return the grade for a given word count."""
    for grade, threshold in sorted(GRADE_THRESHOLDS.items(), key=lambda x: -x[1]):
        if word_count >= threshold:
            return grade
    return "grade0"


def get_language_samples(language: str) -> list:
    """Get all samples for a specific language."""
    if language in NATIVE_LANGUAGE_SAMPLES:
        return NATIVE_LANGUAGE_SAMPLES[language]
    return []


